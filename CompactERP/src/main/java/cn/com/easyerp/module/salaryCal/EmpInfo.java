 package cn.com.easyerp.module.salaryCal;

public class EmpInfo
 {
   private String emp_id;
   private String name;
   private String emp_number;
   private double base_salary;
   private double performance_salary;
   
   public String getEmp_id() { return this.emp_id; }
   private double subsidy; private double allDaysAward; private double loss; private double late; private String is_company_deputy; private String is_foreign;
   
   public void setEmp_id(String emp_id) { this.emp_id = emp_id; }
 
 
 
   
   public String getName() { return this.name; }
 
   
   public void setName(String name) { this.name = name; }
 
   
   public String getEmp_number() { return this.emp_number; }
 
   
   public void setEmp_number(String emp_number) { this.emp_number = emp_number; }
 
   
   public double getBase_salary() { return this.base_salary; }
 
   
   public void setBase_salary(double base_salary) { this.base_salary = base_salary; }
 
   
   public double getPerformance_salary() { return this.performance_salary; }
 
   
   public void setPerformance_salary(double performance_salary) { this.performance_salary = performance_salary; }
 
   
   public double getSubsidy() { return this.subsidy; }
 
   
   public void setSubsidy(double subsidy) { this.subsidy = subsidy; }
 
   
   public String getIs_company_deputy() { return this.is_company_deputy; }
 
   
   public void setIs_company_deputy(String is_company_deputy) { this.is_company_deputy = is_company_deputy; }
 
   
   public String getIs_foreign() { return this.is_foreign; }
 
   
   public void setIs_foreign(String is_foreign) { this.is_foreign = is_foreign; }
 
   
   public double getAllDaysAward() { return this.allDaysAward; }
 
   
   public void setAllDaysAward(double allDaysAward) { this.allDaysAward = allDaysAward; }
 
   
   public double getLoss() { return this.loss; }
 
   
   public void setLoss(double loss) { this.loss = loss; }
 
   
   public double getLate() { return this.late; }
 
   
   public void setLate(double late) { this.late = late; }
 }


