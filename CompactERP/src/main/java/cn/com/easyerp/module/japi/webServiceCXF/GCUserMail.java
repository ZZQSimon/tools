 package cn.com.easyerp.module.japi.webServiceCXF;

public class GCUserMail
 {
   private String username;
   private String e_mail;
   private String telephon;
   private String msg;
   
   public String getUsername() { return this.username; }
 
 
   
   public void setUsername(String username) { this.username = username; }
 
 
   
   public String getE_mail() { return this.e_mail; }
 
 
   
   public void setE_mail(String e_mail) { this.e_mail = e_mail; }
 
 
   
   public String getTelephon() { return this.telephon; }
 
 
   
   public void setTelephon(String telephon) { this.telephon = telephon; }
 
 
   
   public String getMsg() { return this.msg; }
 
 
   
   public void setMsg(String msg) { this.msg = msg; }
 }


